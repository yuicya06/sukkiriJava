package sukkiri.lesson02.code01;

public class Main {
	public static void main(String[] args) {
		int a;
		int b;
		a = 20;
		b = a + 5;
		System.out.println(a);
		System.out.println(b);
		
		int x = 0x11;
		int y = 0b11;
		int z = 011;
			
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
			
		
		/*  0x 16進数の11をを10進数に
			0b 2進数の11をを10進数に
			0  8進数の11をを10進数に				*/
	}

}
