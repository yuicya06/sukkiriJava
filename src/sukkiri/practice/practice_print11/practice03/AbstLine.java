package sukkiri.practice.practice_print11.practice03;

public abstract class AbstLine extends Shape{
	
	/*field*/
	private int length; //直線の幅
	
	/*constructor*/
	///////////*****************
	public AbstLine(int length) {
		this.length = length;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}
	
	
	
	

}
