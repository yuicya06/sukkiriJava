package sukkiri.lesson01.practice.practice03;

public class Main {
	public static void main(String[] args) {
		/*変数の初期化
		 * 　型　変数名　＝　値；
		 */
		
		//①true（型　変数名　＝ true;）
		boolean isValid = true;		
		
		//②'駆'（型　変数名　＝'駆' ;）
		char character = '駆';
		
		//③3.14（型　変数名　＝ 3.14;）
		double pi = 3.14;
		
		//④3141....（型　変数名　＝ 3.141...L;）
		//int i = 314159265853979; //intの範囲を超えてるためエラー
		long x = 3141592658539796L;
		
		//⑤文字列（型　変数名　＝ "文字列";）
		String str = "ミナトの攻撃！敵に15ポイントのダメージを与えた。";
	}

}